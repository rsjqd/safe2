with col3:
    import plotly.graph_objects as go

    # 准备数据
    years = ["2023", "2024", "2025"]
    risk_index = [45000, 63000, 107000]
    illegal_mapping = [8000, 14400, 25900]  # 为了让红蓝高度对比更明显，我帮你把蓝色数据乘以100进行归一化（注意答辩时说明这是放大后的指数）

    fig = go.Figure()

    # 红色柱
    fig.add_trace(go.Bar(
        x=years, y=risk_index,
        name='安全风险基准指数',
        marker_color='rgb(200, 50, 50)'
    ))

    # 蓝色柱
    fig.add_trace(go.Bar(
        x=years, y=illegal_mapping,
        name='非法测绘案件(放大100倍展示)',
        marker_color='rgb(100, 150, 200)'
    ))

    fig.update_layout(
        title="📈 地理信息数据安全风险趋势预判",
        barmode='group',  # 改为 group，红蓝就会并排站在一起，不会重叠
        legend=dict(x=0, y=1.1, orientation="h")
    )

    st.plotly_chart(fig, use_container_width=True)
    st.caption("数据说明：《2024–2026年数据安全态势分析报告》、国家安全机关历年发布的《非法测绘案件查处情况通报》")

with col4:
    # 制作一个极具“智能科学专业”逼格的 3D 散点图，模拟 AI 点云重建效果
    st.markdown("#### 🌐 AI多视图三维重建模拟演示(Point Cloud)")
    st.caption("拖动鼠标可以旋转查看。这模拟了AI如何用网民零散的2D照片，生成立体的军港地形模型。")

    # 生成模拟的3D坐标点
    np.random.seed(42)
    x = np.random.standard_normal(500)
    y = np.random.standard_normal(500)
    z = np.exp(-(x ** 2 + y ** 2) / 2) * 10  # 模拟一个小山丘/雷达罩的形状

    fig_3d = px.scatter_3d(x=x, y=y, z=z, color=z,
                           color_continuous_scale="Viridis",
                           opacity=0.7)
    fig_3d.update_layout(margin=dict(l=0, r=0, b=0, t=0))
    st.plotly_chart(fig_3d, use_container_width=True)

